public class ItemExceptionData {
    private String total;
    private String dealTotal;
    private String time;
    private String nDealTotal;
    private String spendTime;

    public String getTotal() {
        return total;
    }

    public void setTotal(String total) {
        this.total = total;
    }

    public String getDealTotal() {
        return dealTotal;
    }

    public void setDealTotal(String dealTotal) {
        this.dealTotal = dealTotal;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getnDealTotal() {
        return nDealTotal;
    }

    public void setnDealTotal(String nDealTotal) {
        this.nDealTotal = nDealTotal;
    }

    public String getSpendTime() {
        return spendTime;
    }

    public void setSpendTime(String spendTime) {
        this.spendTime = spendTime;
    }
}
